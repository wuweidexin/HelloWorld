package com.chen.javase.thinkinginjava.bookexample.containerindepth.hash;
/**
 * @author chen
 */
public class RectangleNotOR {
	private float width;
	private float height;
	
	public RectangleNotOR() {
		super();
	}
	
	public RectangleNotOR(float width, float height) {
		super();
		this.width = width;
		this.height = height;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}
}
